export const getStatusIndex = (statusId) => orderStatuses.findIndex(s => s.id === statusId);
export const getNextStatus = (statusId) => {
  const idx = getStatusIndex(statusId);
  return idx < orderStatuses.length - 1 ? orderStatuses[idx + 1] : null;
};

// Generate order reference
export const generateOrderRef = () => {
  const year = new Date().getFullYear();
  const num = Math.floor(Math.random() * 9000) + 1000;
  return `PD-${year}-${num}`;
};

